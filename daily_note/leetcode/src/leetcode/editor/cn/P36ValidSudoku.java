class Solution {
    public boolean isValidSudoku(char[][] board) {
        Map<Character, Integer>[] row = new HashMap[9];
        Map<Character, Integer>[] col = new HashMap[9];
        Map<Character, Integer>[] table = new HashMap[9];

        for (int i = 0; i < 9; i++) {
            row[i] = new HashMap<>();
            col[i] = new HashMap<>();
            table[i] = new HashMap<>();
        }

        int t;
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                if ('.' == board[i][j]) {
                    continue;
                }

                if (row[i].get(board[i][j]) != null) {
                    return false;
                }
                row[i].put(board[i][j], 1);

                if (col[j].get(board[i][j]) != null) {
                    return false;
                }
                col[j].put(board[i][j], 1);

                t = i / 3 + j / 3 * 3;
                if (table[t].get(board[i][j]) != null) {
                    return false;
                }
                table[t].put(board[i][j], 1);
            }
        }
        return true;
    }
}
